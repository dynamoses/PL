export class Player {
    constructor(name, position, appearances, goals, assists, goalsPerMatch, passesPerMin) {
        this.name = name;
        this.position = position;
        this.appearances = appearances;
        this.goals = goals;
        this.assists = assists;
        this.goalsPerMatch = goalsPerMatch;
        this.passesPerMin = passesPerMin;
    }
}
